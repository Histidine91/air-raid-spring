local moveDefs = {
	TANK = {
		maxwaterdepth=1,
		maxslope=30,
		crushstrength=0,
		footprintx=1,
		footprintz=1,
	},
	BOAT = {
		minwaterdepth=1,
		maxslope=30,
		crushstrength=0,
		footprintx=1,
		footprintz=1,
	},
	BIGBOAT = {
		minwaterdepth=1,
		maxslope=30,
		crushstrength=0,
		footprintx=1,
		footprintz=2,
	},
}

--------------------------------------------------------------------------------
--------------------------------------------------------------------------------

-- convert from map format to the expected array format

local array = {}
local i = 1
for k,v in pairs(moveDefs) do
	v.heatmapping = false -- disable heatmapping
	array[i] = v
	v.name = k
	i = i + 1
end


--------------------------------------------------------------------------------
--------------------------------------------------------------------------------

return array

